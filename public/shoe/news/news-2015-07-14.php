<h2>'เบรกเกอร์ ฟุตซอล เอฟเอคัพ 2015' พร้อมระเบิดแข้ง ต.ค.นี้</h2>

<h3>14 กรกฎาคม 2558</h3>

ทัวร์นาเม้นท์ที่ไม่มีที่ว่างสำหรับผู้แพ้ เตรียมกลับมาฟาดแข้งกันในปี 2015 เมื่อ "เบรกเกอร์"เข้ามา เป็นผู้สนับสนุนหลัก ร่วมกับ สมาคมฟุตบอลแห่งประเทศไทย และ บมจ.สยามอินเตอร์มัลติมีเดีย จำกัด พร้อมเตรียมแข่งขันในรอบคัดเลือกเดือนตุลาคมนี้เป็นต้นไป<br>

<br>

ปีที่ผ่านมาการแข่งขันฟุตซอลในรายการเอฟเอคัพ ถือว่าได้กระแสตอบรับจากคอโต๊ะเล็กกันมาก ด้วยการแข่งขันที่เป็นไปอย่างเข้มข้น ที่แต่ละทีมต้องผลงานให้ดีที่สุดเพื่อคว้าชัยชนะให้ได้ เนื่องจากทัวนาเม้นท์ดังกล่าวไม่มีที่วางสำหรับทีมที่แพ้ โดยเป็นทีมดังอย่าง ชลบุรี บลูเวฟ ที่คว้าแชมป์ไปครองในปีนั้น 

 <br><br>

ล่าสุดความสนุกสุดเร้าใจกำลังจะกลับมา เมื่อวันที่ 14 กรกฎาคม 2558 ได้มีงานแถลงข่าวการแข่งขัน "เบรกเกอร์ ฟุตซอล เอฟเอคัพ 2015" ที่ ชั้น 3 ห้องปาริชาติ โรงแรมโกลเด้นทิวลิป ซอฟเฟอริน พระราม 9 โดยมีนาย วิษณุ วงศ์วีระนนท์ชัย รองกรรมการผู้จัดการบริษัท เอส.ซี.เอส สปอร์ตสแวร์ จำกัด, พันเอกวรวุฒิ ทองศรีงาม เลขาธิการสมาคมฟุตบอลแห่งประเทศไทยฯ, นายมนตรี ไชยพันธุ์ รองผู้ว่าการกีฬาแห่งประเทศไทย ฝ่ายกีฬาเป็นเลิศ และวิทยาศาสตร์การกีฬา และนายวิฑูร นิรันตราย ประธานกรรมการบริหาร บมจ.สยามอินเตอร์มัลติมีเดีย จำกัด เป็นประธาน พร้อมด้วย แขกผู้มีเกียรติ และสื่อมวลชนร่วมงานคับคั่ง

 <br><br>

โดยการแข่งขันฟุตซอลเอฟเอคัพในครั้งนี้จัดขึ้นเป็นปีที่ 6 ติดต่อกัน และเป็นปีที่ 2 ของบมจ.สยามอินเตอร์มัลติมีเดีย จำกัด (SMM) ที่เข้ามารับหน้าที่เป็นฝ่ายจัดการแข่งขัน พร้อมกับได้ เบรกเกอร์ หรือ บริษัท เอส.ซี.เอส สปอร์ตสแวร์ จำกัด เข้ามาเป็นผู้สนับสนุนหลัก โดยเน้นเปิดโอกาสให้ทีมฟุตซอลในระดับประชาชนทั่วไป และทีมในระดับสโมสรอาชีพ ทั้ง ฟุตซอล ไทยแลนด์ พรีเมียร์ลีก และฟุตซอลลีก ดิวิชั่น 1 เข้าร่วมการแข่งขัน 

 <br><br>

นายวิษณุ วงศ์วีระนนท์ชัย กล่าวในงานแถลงข่าวว่า "ในนามของเบรกเกอร์ เราได้เล็งเห็นและให้ความสนใจด้านกีฬา โดยเฉพาะกีฬาฟุตซอล ถือเป็นกีฬาที่ได้รับความนิยมอย่างมากในปัจจุบัน ผมยินดีที่จะได้เป็นส่วนหนึ่ง ในการร่วมสนับสนุนผลักดันกีฬาฟุตซอล โดยเป็นผู้สนับสนุนหลักตลอดการแข่งขันในครั้งนี้"

 <br><br>

พันเอกวรวุฒิ ทองศรีงาม กล่าวต่อว่า "จากความสำเร็จในครั้งที่ผ่านมา การแข่งขันครั้งนี้ถือเป็นการสานต่อนโยบายของทางสมาคมฟุตบอลแห่งประเทศไทย ที่ผลักดันให้เกิดขึ้นอย่างต่อเนื่อง ทำให้กีฬาฟุตซอลไทยเป็นที่รุ้จักในวงกว้าง เพื่อพัฒนาศักยภาพของนักกีฬาต่อไปในอนาคต"

 <br><br>

นายมนตรี ไชยพันธุ์ กล่าวว่า "การกีฬาแห่งประเทศไทย ซึ่งมุ่งพัฒนาความเป็นเลิศ และกีฬาเพื่ออาชีพ จะสนับสนุนในทุกด้านเพื่อให้การแข่งขันในครั้งนี้ ประสบความสำเร็จ และเชื่อมั่นว่าแฟนๆฟุตซอล จะสนุกตื่นเต้น เร้าใจไปกับการแข่งขันเบรกเกอร์ฟุตซอล เอฟเอคัพ 2015 ในครั้งนี้อย่างแน่นอน"

 <br><br>

นายวิฑูร นิรันตราย กล่าวปิดท้ายว่า "ในฐานะ SMM ซึ่งผลิตข่าวกีฬามาเกือบ 30 ปี จะใช้ทุกสื่อที่มีอยู่ ไม่ว่าจะเป็น SMMTV,FM 96 SPORT RADIO,สื่ออินเตอร์เน็ต เว็บไซต์ SMM SPORT รวมไปถึงโซเชียลมีเดียต่างๆ ในการดำเนินการถ่ายทอดสดการแข่งขันเพื่อให้แฟนฟุตซอลทั่วประเทศสามารถติดตามการแข่งขันรายการนี้ผ่านทาง SMM"

 <br><br>

อย่างไรก็ตามรูปแบบการแข่งขัน "เบรกเกอร์ ฟุตซอล เอฟเอคัพ 2015" จะเป็นการแข่งในระบบน๊อคเอ๊าต์แพ้คัดออก โดยจะเปิดรับสมัครตั้งแต่วันที่ 20 กรกฎาคม-20 กันยายน 2558 และแข่งขันรอบคัดเลือกตั้งแต่วันที่ 11-25 ตุลาคม ที่มหาวิทยาลัยเกษมบัณฑิต วิทยาเขตร่มเกล้า แข่งขันรอบสุดท้ายตั้งแต่วันที่ 5-15 พฤศจิกายน 2558 ที่ศูนย์การค้าแฟชั่นไอซ์แลนด์ 

<br><br>

พร้อมกันนี้ทางฝ่ายจัดการแข่งขันจะเปิดสำนักงานที่ อาคาร SMM ซอยลาดพร้าว 48 เพื่อเป็นศูนย์กลางในการประสานงาน กับสโมสรต่างๆอีกด้วย โดยสามารถสอบถามรายละเอียดเพิ่มเติมในการส่งทีมเข้าร่วมแข่งขัน โทร 02-276-9435, 02-277-2837, 090-008-1519 และติดตามข่าวสารความเคลื่อนไหวได้ทางแฟนเพจฟุตซอลไทย www.facebook.com/ThaiFutsal 



