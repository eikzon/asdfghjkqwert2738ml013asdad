<h2>รองเท้า-หอสมุดให้ โรงเรียนวัดสระแก้ว “ชีวิตที่มีคุณค่า คือ ชีวิตที่ใช้เพื่อผู้อื่น”</h2>

<h3>22 พฤษภาคม 2558</h3>

ด้วยประโยคสั้นๆ นี้เองทำให้ คุณรุจิรพรรณ จึงรุ่งเรืองกิจ รองประธานกรรมการบริหารสนามพัฒนากอล์ฟคลับ แอนด์ รีสอร์ท ชักชวนพี่ชายที่แสนดี คือ คุณธนาธร จึงรุ่งเรืองกิจ รองประธานกลุ่มไทยซัมมิท ที่มีใจรักทางด้านกีฬาการวิ่งเหมือนกัน ริเริ่มกิจกรรมที่จะเชื่อมโยงกีฬาและการช่วยเหลือสังคมเข้าด้วยกันจึงได้จัดทำแคมเปญ TJ Run for Charity เพื่อระดมทุนและหารายได้ไปช่วยเหลือผู้ด้อยโอกาสในสังคมตามความตั้งใจของทั้ง 2 ท่าน โดยคุณธนาธร ได้ร่วมแข่งขันรายการมาราธอนสุดโหด ระยะทาง 560 กิโลเมตร ที่อุณหภูมิเฉลี่ยลบ 30 องศา บริเวณอาร์กติก ใกล้กับขั้วโลกเหนือ ณ ประเทศแคนาดา ซึ่งท่านได้ชักชวนผู้มีจิตศรัทธาร่วมลงชื่อระดมทุน โดยทุกๆ 1 กิโลเมตร ที่วิ่งได้ขอให้ร่วมกันบริจาคเงินคนละ 10 บาท โดยเงินที่ได้จะนำไปบริจาครองเท้านักเรียนและห้องสมุดให้กับโรงเรียนวัดสระแก้ว (รุ่งโรจน์ธนกุล อุปถัมภ์) จ.อ่างทอง หรือที่รู้จักกันในนามสถานสงเคราะห์เด็กกำพร้าวัดสระแก้ว ในชื่อ “โครงการรองเท้าและห้องสมุดเพื่อน้อง” โดยคุณธนาธร สามารถแข่งขันจนจบการแข่งขันได้สำเร็จ ถือเป็นคนไทยคนแรกที่สามารถผ่านเข้าเส้นชัยในการแข่งขันครั้งนี้ได้

<br><br>

และเมื่อวันที่ 22 พฤษภาคม 2558 ที่ผ่านมา คุณรุจิรพรรณ จึงรุ่งเรืองกิจ รองประธานสนามพัฒนากอล์ฟคลับ แอนด์ รีสอร์ท จึงรวบรวมเงินที่ได้จากการจัดทำแคมเปญในครั้งนี้ ชักชวนพันธมิตรที่ดีอย่างมูลนิธิไทยซัมมิทพัฒนา ซึ่งถือได้ว่าเป็นตัวแทนของโครงการ TJ Run for Charity และบริษัท เอส.ซี.เอส สปอร์ตสแวร์ จำกัด หรือที่รู้จักกันในนามรองเท้านักเรียนยี่ห้อเบรกเกอร์ บริจาครองเท้านักเรียนต้อนรับเปิดเทอมจำนวน 1,311 คู่ และบริจาคเงินปรับปรุงห้องสมุดและสร้างห้องอาบน้ำมูลค่า 1,720,000 บาท โดยมีคุณณัฎฐ์ธาตา วงศ์วีระนนท์ชัย รองประธานกรรมการ บริษัท เอส.ซี.เอส สปอร์ตสแวร์ จำกัด และ คุณสมชัย เหลืองวารี เลขานุการและกรรมการมูลนิธิไทยซัมมิทพัฒนา ร่วมเป็นสักขีพยานในการมอบรองเท้านักเรียนและเงินบริจาคในงานบุญครั้งนี้ โดยมี ดร.พระมหาไพเราะ ฐิตสีโล (กฤษณา) เจ้าอาวาสและผู้รับใบอนุญาตโรงเรียนวัดสระแก้ว (รุ่งโรจน์ธนกุล อุปถัมภ์) และเหล่าคณาจารย์ให้การต้อนรับคณะผู้บริจาคอย่างอบอุ่นด้วยการแสดงดนตรีวง Symphonic Band ของนักเรียนโรงเรียนวัดสระแก้ว ทำเอาคณะผู้บริจาคถึงกับยิ้มไม่หุบเรียกว่าอิ่มแอมใจกันทั้งผู้ให้และผู้รับ

<br><br>

คุณรุจิรพรรณ ยังฝากถึงผู้ที่มีกำลังทรัพย์หรือกำลังศรัทธาพอที่จะหยิบยื่นโอกาสให้กับสังคมว่า อยากให้ทุกคนในสังคมได้รู้จักการแบ่งบัน ยังมีอีกเด็กๆ อีกหลายคนที่ยังขาดโอกาสทางการศึกษา จึงอยากเชิญชวนให้ทุกท่านเป็นส่วนหนึ่งที่จะหยิบยื่นโอกาสดีๆ ให้กับเด็กๆ เหล่านั้น ซึ่งต่อไปภายภาคหน้าเด็กๆ เหล่านั้นอาจจะได้เป็นกำลังสำคัญในการพัฒนาประเทศต่อไปในอนาคตและขอบพระคุณมายังทุกท่านที่ร่วมสมทบทุนและบริจาคเงินให้แก่กิจกรรมในครั้งนี้ โดยทางสนามพัฒนากอล์ฟคลับ แอนด์ รีสอร์ท ได้เป็นตัวแทนของทุกท่านนำเงินทุกบาททุกสตางค์มามอบให้ตามความประสงค์ที่ท่านได้ร่วมอนุโมทนาบุญไว้ทุกประการ และหวังว่าจะได้รับการสนับสนุนจากทุกท่านๆ ในกิจกรรมต่อๆ ไป



